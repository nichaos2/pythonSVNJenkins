def sum(l_arg):
    '''
    add all the arguments in a list
    simple function to tests
    from https://realpython.com/python-testing/
    '''
    total = 0
    for val in l_arg:
        total += val
    return total

def concatanate(l_arg):
    'adds all values to a message'
    total = ''
    for val in l_arg:
        total += str(val)
    return total

